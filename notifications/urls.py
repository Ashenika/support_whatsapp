from django.contrib import admin
from django.urls import path, re_path
from rest_framework import permissions
from drf_yasg.views import get_schema_view
from drf_yasg import openapi
from messaging.views import send_message, webhook, user_login, user_logout, list_messages, view_message

schema_view = get_schema_view(
    openapi.Info(
        title="WhatsApp Messaging API",
        default_version='v1',
        description="API documentation for WhatsApp messaging app",
        terms_of_service="https://www.google.com/policies/terms/",
        contact=openapi.Contact(email="your_email@example.com"),
        license=openapi.License(name="MIT License"),
    ),
    public=True,
    permission_classes=(permissions.AllowAny,),
)

urlpatterns = [
    path('admin/', admin.site.urls),
    path('login/', user_login, name='login'),
    path('send-message/', send_message, name='send_message'),
    path('logout/', user_logout, name='logout'),
    path('webhook/', webhook, name='webhook'),
    path('messages/', list_messages, name='list_messages'),
    path('messages/<int:message_id>/', view_message, name='view_message'),
    re_path(r'^swagger(?P<format>\.json|\.yaml)$', schema_view.without_ui(cache_timeout=0), name='schema-json'),
    path('swagger/', schema_view.with_ui('swagger', cache_timeout=0), name='schema-swagger-ui'),
    path('redoc/', schema_view.with_ui('redoc', cache_timeout=0), name='schema-redoc'),
]
